    public SelectPort(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();
        this.setTitle("Seleccion de puerto del arduino");
        this.setIconImage(Toolkit.getDefaultToolkit().
         getImage(ClassLoader.getSystemResource("images/cuboIco.png")));
        rellenarPorts();
        setLocationRelativeTo(null);

        // Close the dialog when Esc is pressed
        String cancelName = "cancel";
        InputMap inputMap = getRootPane().getInputMap(JComponent.WHEN_ANCESTOR_OF_FOCUSED_COMPONENT);
        inputMap.put(KeyStroke.getKeyStroke(KeyEvent.VK_ESCAPE, 0), cancelName);
        ActionMap actionMap = getRootPane().getActionMap();
        actionMap.put(cancelName, new AbstractAction() {
            public void actionPerformed(ActionEvent e) {
                doClose(RET_CANCEL);
            }
        });
    }

    /**
     * Se encarga de rellenar el ComboBox de puertos disponibles.
     */
    private void rellenarPorts(){
        PanamaHitek_Arduino ino = new PanamaHitek_Arduino();
        List<String> ports = ino.getSerialPorts();
        for (String port : ports) {
            this.cb_portsAbles.addItem(port);
        }
    }
    
    public String getPort(){
        return this.cb_portsAbles.getSelectedItem().toString();
    }

    public int getReturnStatus() {
        return returnStatus;
    }


